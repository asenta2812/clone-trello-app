const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const roleValidation = require('../../validations/role.validation');
const roleController = require('../../controllers/role.controller');
const { DocumentType, SecurityOperations } = require('../../config/constants');

const router = express.Router();

router
  .route('/')
  .post(auth(DocumentType.roles, SecurityOperations.add), validate(roleValidation.createRole), roleController.createRole)
  .get(auth(DocumentType.roles), validate(roleValidation.getRoles), roleController.getRoles);

router
  .route('/:roleId')
  .get(auth(DocumentType.roles), validate(roleValidation.getRole), roleController.getRole)
  .patch(auth(DocumentType.roles, SecurityOperations.update), validate(roleValidation.updateRole), roleController.updateRole)
  .delete(
    auth(DocumentType.roles, SecurityOperations.delete),
    validate(roleValidation.deleteRole),
    roleController.deleteRole
  );

module.exports = router;

